#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;

	j = 0;
	i = 0;
	while (i < dstsize && dst[i])
		i++;
	if (i == dstsize || dstsize < 0)
		return (i + ft_strlen(src));
	while (j < (dstsize - i - 1) && src[j])
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	return (i + ft_strlen((char *)src));
}
